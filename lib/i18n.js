export function useT(lang) {
  return (key, vars) => {
    let s = (I18N[lang] && I18N[lang][key]) || key;
    if (vars) Object.keys(vars).forEach(k => { s = s.replace(`{${k}}`, vars[k]); });
    return s;
  };
}
